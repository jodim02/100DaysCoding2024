package Coding100Hari;

public class Day33 {
    
    public static void main(String[] args) {
        
        //Widening Casting
        int A = 33;
        double B = A;
        
        //Narrowing Casting
        double C = 33.0;
        int D = (int) C;
        
        //Output
        System.out.println("Nilai 1 : " + B);
        System.out.println("Nilai 2 : " + D);
    }
    
}
