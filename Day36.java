package Coding100Hari;

public class Main {
    
    public static void main(String[] args) {
        //Deklarasi variabel:
        int a = 30;
        int b = 6;
        int c = 4;
        
        //Operasi:
        int kali = a * b;//Perkalian
        int bagi = a / b;//Pembagian
        int sisaBagi = a % c;//Modulus
        
        //Output
        System.out.println("Hasil a x b = " + kali);
        System.out.println("Hasil a / b = " + bagi);
        System.out.println("Hasil a % c = " + sisaBagi);
    }
    
}
