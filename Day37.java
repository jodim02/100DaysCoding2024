package Coding100Hari;

public class Day37{

    public static void main(String[] args) {
        // Operator Penugasan
        // Pengisian Nilai 
        int x = 37;
        System.out.println("Nilai x = " + x);
        // Pengisian dan Penambahan 
        int y = 30;
        y += 7;
        System.out.println("Penambahan nilai x = " + y);
        // Pengisian dan Pengurangan
        int z = 37;
        z -= 7;
        System.out.println("Pengurangan nilai z " + z);
    }
    
}
